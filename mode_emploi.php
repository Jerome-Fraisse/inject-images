<div class="wrap">
    <h1>Mode d'emploi du plugin Inject Images</h1>

    <h2>1. Installation du plugin</h2>
    <p>
        Téléversez le fichier ZIP du plugin depuis l'interface d'administration de WordPress. Une fois installé, activez le plugin
        pour commencer à l'utiliser.
    </p>

    <h2>2. Utilisation du plugin</h2>
    <p>
        Après l'activation, vous trouverez un nouveau menu intitulé "Inject Images" dans le menu d'administration.
    </p>

    <h3>2.1. Téléverser des images</h3>
    <ul>
        <li>Accédez à la page d'administration du plugin via "Inject Images".</li>
        <li>Sélectionnez plusieurs images à téléverser dans le champ prévu à cet effet.</li>
        <li>Cliquez sur "Téléverser" pour envoyer les images dans le répertoire "stock-images".</li>
    </ul>

    <h3>2.2. Aperçu des images</h3>
    <ul>
        <li>Les images téléversées dans le répertoire "stock-images" apparaissent dans la section "Images disponibles".</li>
        <li>Les images déjà utilisées seront déplacées vers "images-utilisees" et peuvent être consultées dans cette section.</li>
    </ul>

    <h2>3. Utilisation du shortcode</h2>
    <p>
        Vous pouvez utiliser le shortcode suivant pour insérer une image aléatoire dans un article ou une page :
    </p>
    <code>[inject_images]</code>

    <h2>4. Configuration supplémentaire</h2>
    <p>
        Si vous avez besoin de configurer ou d'ajuster le plugin, vous pouvez modifier les fichiers directement dans le répertoire du plugin.
    </p>
</div>